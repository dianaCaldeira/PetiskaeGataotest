// Helper function to generate page metadata
export function generatePageMetadata(
  pageKey: keyof typeof seoContent,
  customTitle?: string,
  customDescription?: string,
  customImage?: string
): Metadata {
  const pageData = seoContent[pageKey];
  const title = customTitle || pageData.title;
  const description = customDescription || pageData.description;
  const image = customImage || '/og-image.jpg';

  return {
    title,
    description,
    keywords: pageData.keywords,
    openGraph: {
      title,
      description,
      url: `${baseUrl}/${pageKey === 'home' ? '' : pageKey}`,
      images: [
        {
          url: image,
          width: 1200,
          height: 630,
          alt: title,
        },
      ],
    },
    twitter: {
      title,
      description,
      images: [image],
    },
  };
}

